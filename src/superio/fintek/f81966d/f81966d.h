#define GPIO0X_DRIVE_ENABLE_REGISTER	0xF3
#define GPIO1X_DRIVE_ENABLE_REGISTER	0xE3
#define GPIO3X_DRIVE_ENABLE_REGISTER	0xC3
#define GPIO4X_DRIVE_ENABLE_REGISTER	0xB3
