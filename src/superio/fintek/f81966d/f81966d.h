#define GPIO7X_DRIVE_ENABLE_REGISTER	0x83
#define GPIO8X_DRIVE_ENABLE_REGISTER	0x8B
#define GPIO9X_DRIVE_ENABLE_REGISTER	0x9B

#endif /* SUPERIO_FINTEK_F81966_H */
